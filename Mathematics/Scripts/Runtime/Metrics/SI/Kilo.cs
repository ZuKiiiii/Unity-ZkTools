namespace ZkTools.Mathematics.Metrics.SI
{
	public struct Kilo : IUnit
	{
		#region ==============================[Constants]==============================

			public const string Base10 = "10e+3";

			public const string Name = "Kilo";

			public const string Symbole = "k";

			public const float Value = 10e+3f;

		#endregion

		#region ==============================[Constants]==============================

			public string GetBase10()
			{
				return Base10;
			}

			public string GetName()
			{
				return Name;
			}

			public string GetSymbole()
			{
				return Symbole;
			}

			public float GetValue()
			{
				return GetValue();
			}

		#endregion

	}
}